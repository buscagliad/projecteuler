void addthem(long *g, geonum_t gt)
{
	while (*g >= 0)
	{
		if (*g != 0)
		{
			geonum gn;
			gn.ct = gt;
			gn.num = *g;
			gn.low = LOW(*g);
			gn.high = HIGH(*g);
			glist.push_back(gn);
		}
		g++;
	}
}

