#define LOW(x)	(x)%100
#define HIGH(x)	(x)/100

bool	low_check(long v, long *a)
{
	while (*a > 0)
	{
		if (v == LOW(*a)) return true;
		a++;
	}
	return false;
}

bool	high_check(long v, long *a)
{
	while (*a > 0)
	{
		if (v == HIGH(*a)) return true;
		a++;
	}
	return false;
}
void remdigs(long *a, long *b, long *c, long *d, long *e, long *f)
{
	// eliminate a[n] if LOW[a[n]] does NOT appear in any of the b, c, d, e, f HIGH
	//      AND       if HIGH(a[n]) does NOT appear in any of the b, c, d, e, f, LOW
	bool low_found = false;
	bool high_found = false;
	while (*a > 0)
	{
		low_found = low_check(HIGH(*a), b) || low_check(HIGH(*a), c) ||
					low_check(HIGH(*a), d) || low_check(HIGH(*a), e) || 
					low_check(HIGH(*a), f);
		high_found = high_check(LOW(*a), b) || high_check(LOW(*a), c) ||
					 high_check(LOW(*a), d) || high_check(LOW(*a), e) ||
					 high_check(LOW(*a), f);
		if (!low_found || !high_found) { 
			printf("%ld eliminated\n", *a); 
			*a = 0; 
		}
		a++;
	}
}

