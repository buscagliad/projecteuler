long isSquare(long n)
{
	long sa = sqrt((double) n);
	if (sa * sa == n) return sa;
	return 0;
}

long getSquare(long n)
{
	return n * n;
}

long isTriangle(long p)
{
	long t = isSquare(1 + 8 * p);
	if (t) return (t - 1) / 2;
	return 0;
}

long getTriangle(long n)
{
	return n * (n + 1) / 2;
}

long isPentagonal(long p)
{
	long t = isSquare(1 + 24 * p);
	if (!t) return 0;
	t += 1; // is t divisible by six??
	if (t % 6 == 0) return t / 6;
	return 0;
}

long getPentagonal(long n)
{
	return n * (3 * n - 1) / 2;
}

long isHexagonal(long p)
{
	long t = isSquare(1 + 8 * p);
	if (!t) return 0;
	t += 1; // is t divisible by four??
	if (t % 4 == 0) return t / 4;
	return 0;
}

long getHexagonal(long n)
{
	return n * ( 2 * n - 1 );
}

long isHeptagonal(long p)
{
	long t = isSquare(9 + 40 * p);
	if (!t) return 0;
	t += 3; // is t divisible by 10 ??
	if (t % 10 == 0) return t / 10;
	return 0;
}

long getHeptagonal(long n)
{
	return n * (5 * n - 3 ) / 2;
}

long isOctagonal(long p)
{
	long t = isSquare(4 + 12  * p);
	if (!t) return 0;
	t += 2; // is t divisible by 6 ??
	if (t % 6 == 0) return t / 6;
	return 0;
}

long getOctagonal(long n)
{
	return n * ( 3 * n - 2 );
}


